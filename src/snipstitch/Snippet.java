package snipstitch;


public class Snippet {
	
	private Double startTime;
	private Double stopTime;
	
	public Snippet() {
		
	}

	public Double getStartTime() {
		return startTime;
	}
	
	public Double getStopTime() {
		return stopTime;
	}
	
	public void setStartTime(Double newStart) {
		startTime = newStart;
	}
	
	public void setStopTime(Double newStop) {
		stopTime = newStop;
	}
	
}
